#!/usr/bin/env bash
set -u -o pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
EVAL_DIR="${REPO_ROOT}/evaluation"

usage() {
  cat <<'EOF'
Usage:
  bash evaluation/run_openvla_reference_eval.sh \
    --bench-root /path/to/RoboCerebraBench \
    --checkpoint moojink/openvla-7b-oft-finetuned-libero-spatial-object-goal-10 \
    --gpu-ids 0,1

Required:
  --bench-root PATH
      RoboCerebra benchmark root.

Optional:
  --checkpoint CHECKPOINT
      OpenVLA / OpenVLA-OFT checkpoint path or Hugging Face repo id.
      Defaults to moojink/openvla-7b-oft-finetuned-libero-spatial-object-goal-10.

  --init-files-root PATH
      Defaults to <bench-root>/init_files.

  --task-types CSV
      Defaults to all six RoboCerebra task categories.

  --num-trials N
      Defaults to 5.

  --task-suite-name NAME
      OpenVLA action un-normalization key / task suite name.
      For LIBERO OpenVLA-OFT reference checkpoints, inspect model.norm_stats
      and pass the matching key, for example libero_spatial_no_noops.
      Defaults to eval_openvla.py's config default.

  --gpu-ids CSV
      Comma-separated visible GPU ids to round-robin over. Defaults to 0.

  --eval-log-dir PATH
      Defaults to <repo>/experiments/logs.

  --rollout-root PATH
      Defaults to sibling directory next to eval-log-dir named eval_rollouts.

  --run-note-prefix TEXT
      Prefix used for generated job names and summary files.
      Defaults to openvla_reference.

Example:
  nohup bash evaluation/run_openvla_reference_eval.sh \
    --bench-root /gs/bs/tga-shinoda/felid/robocerebra_data/RoboCerebraBench \
    --eval-log-dir /gs/bs/tga-shinoda/felid/eval_logs \
    --rollout-root /gs/bs/tga-shinoda/felid/eval_rollouts \
    --gpu-ids 0,1 \
    > /gs/bs/tga-shinoda/felid/eval_logs/openvla_reference_launcher.log 2>&1 &
EOF
}

CHECKPOINT="moojink/openvla-7b-oft-finetuned-libero-spatial-object-goal-10"
NUM_TRIALS="5"
TASK_TYPES="Ideal,Random_Disturbance,Mix,Observation_Mismatching,Memory_Execution,Memory_Exploration"
TASK_SUITE_NAME=""
GPU_IDS="0"
BENCH_ROOT=""
INIT_FILES_ROOT=""
EVAL_LOG_DIR="${REPO_ROOT}/experiments/logs"
ROLLOUT_ROOT=""
RUN_NOTE_PREFIX="openvla_reference"

while (($#)); do
  case "$1" in
    --bench-root)
      BENCH_ROOT="$2"
      shift 2
      ;;
    --checkpoint)
      CHECKPOINT="$2"
      shift 2
      ;;
    --init-files-root)
      INIT_FILES_ROOT="$2"
      shift 2
      ;;
    --task-types)
      TASK_TYPES="$2"
      shift 2
      ;;
    --num-trials)
      NUM_TRIALS="$2"
      shift 2
      ;;
    --task-suite-name)
      TASK_SUITE_NAME="$2"
      shift 2
      ;;
    --gpu-ids)
      GPU_IDS="$2"
      shift 2
      ;;
    --eval-log-dir)
      EVAL_LOG_DIR="$2"
      shift 2
      ;;
    --rollout-root)
      ROLLOUT_ROOT="$2"
      shift 2
      ;;
    --run-note-prefix)
      RUN_NOTE_PREFIX="$2"
      shift 2
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      echo "error: unknown argument: $1" >&2
      usage
      exit 1
      ;;
  esac
done

if [[ -z "$BENCH_ROOT" ]]; then
  echo "error: --bench-root is required" >&2
  exit 1
fi

if [[ -z "$INIT_FILES_ROOT" ]]; then
  INIT_FILES_ROOT="${BENCH_ROOT}/init_files"
fi

if [[ -z "$ROLLOUT_ROOT" ]]; then
  ROLLOUT_ROOT="$(cd "${EVAL_LOG_DIR}/.." 2>/dev/null && pwd || true)"
  if [[ -z "$ROLLOUT_ROOT" ]]; then
    ROLLOUT_ROOT="${REPO_ROOT}/eval_rollouts"
  else
    ROLLOUT_ROOT="${ROLLOUT_ROOT}/eval_rollouts"
  fi
fi

mkdir -p "$EVAL_LOG_DIR" "$ROLLOUT_ROOT"

IFS=',' read -r -a TASK_ARRAY <<< "$TASK_TYPES"
IFS=',' read -r -a GPU_ARRAY <<< "$GPU_IDS"

if ((${#GPU_ARRAY[@]} == 0)); then
  echo "error: no GPU ids parsed from --gpu-ids" >&2
  exit 1
fi

ensure_robosuite_macros() {
  python - <<'PY'
import sysconfig
from pathlib import Path

macros_path = Path(sysconfig.get_paths()["purelib"]) / "robosuite" / "macros_private.py"
macros_path.parent.mkdir(parents=True, exist_ok=True)
content = (
    "# Auto-generated by evaluation/run_openvla_reference_eval.sh\n"
    "# Disable robosuite file logging on shared cluster nodes.\n"
    "FILE_LOGGING_LEVEL = None\n"
)
current = macros_path.read_text(errors="ignore") if macros_path.exists() else ""
if current != content:
    macros_path.write_text(content)
print(macros_path)
PY
}

trim() {
  local value="$1"
  value="${value#"${value%%[![:space:]]*}"}"
  value="${value%"${value##*[![:space:]]}"}"
  printf '%s' "$value"
}

task_slug() {
  local task_name="$1"
  task_name="${task_name// /_}"
  task_name="${task_name//\//_}"
  printf '%s' "$task_name"
}

DATE_TAG="$(date +%Y%m%d_%H%M%S)"
MASTER_LOG="${EVAL_LOG_DIR}/${RUN_NOTE_PREFIX}_openvla_eval_${DATE_TAG}.log"
MANIFEST="${EVAL_LOG_DIR}/${RUN_NOTE_PREFIX}_openvla_eval_${DATE_TAG}_manifest.tsv"
SUMMARY_CSV="${EVAL_LOG_DIR}/${RUN_NOTE_PREFIX}_openvla_eval_${DATE_TAG}_summary.csv"
SUMMARY_MD="${EVAL_LOG_DIR}/${RUN_NOTE_PREFIX}_openvla_eval_${DATE_TAG}_summary.md"

printf 'label\tstep\ttask\tgpu\tcheckpoint_path\tlog_path\trollout_dir\n' > "$MANIFEST"

echo "Starting OpenVLA reference evaluation at ${DATE_TAG}" | tee "$MASTER_LOG"
echo "Bench root: ${BENCH_ROOT}" | tee -a "$MASTER_LOG"
echo "Init files root: ${INIT_FILES_ROOT}" | tee -a "$MASTER_LOG"
echo "Checkpoint: ${CHECKPOINT}" | tee -a "$MASTER_LOG"
echo "Task types: ${TASK_TYPES}" | tee -a "$MASTER_LOG"
echo "Num trials: ${NUM_TRIALS}" | tee -a "$MASTER_LOG"
if [[ -n "$TASK_SUITE_NAME" ]]; then
  echo "Task suite / unnorm key: ${TASK_SUITE_NAME}" | tee -a "$MASTER_LOG"
fi
echo "GPU ids: ${GPU_IDS}" | tee -a "$MASTER_LOG"
echo "Eval log dir: ${EVAL_LOG_DIR}" | tee -a "$MASTER_LOG"
echo "Rollout root: ${ROLLOUT_ROOT}" | tee -a "$MASTER_LOG"

ROBOSUITE_MACROS_PATH="$(ensure_robosuite_macros)"
echo "Robosuite macros path: ${ROBOSUITE_MACROS_PATH}" | tee -a "$MASTER_LOG"

active_jobs=0
gpu_cursor=0
failed_jobs=0

launch_eval_job() {
  local task="$1"
  local gpu="$2"
  local task_name
  task_name="$(trim "$task")"
  local task_name_slug
  task_name_slug="$(task_slug "$task_name")"
  local job_note="${RUN_NOTE_PREFIX}_openvla_reference_${task_name_slug}_${DATE_TAG}"
  local log_path="${EVAL_LOG_DIR}/${job_note}.log"
  local rollout_dir="${ROLLOUT_ROOT}/${job_note}"

  mkdir -p "$rollout_dir"
  printf '%s\t%s\t%s\t%s\t%s\t%s\t%s\n' \
    "openvla_reference" "hf" "$task_name" "$gpu" "$CHECKPOINT" "$log_path" "$rollout_dir" >> "$MANIFEST"

  echo "Launching task=${task_name} gpu=${gpu}" | tee -a "$MASTER_LOG"
  echo "  checkpoint=${CHECKPOINT}" | tee -a "$MASTER_LOG"
  echo "  log=${log_path}" | tee -a "$MASTER_LOG"

  (
    cd "$EVAL_DIR"
    export CUDA_VISIBLE_DEVICES="$gpu"
    export MUJOCO_GL="${MUJOCO_GL:-egl}"
    eval_args=(
      --model_family openvla \
      --pretrained_checkpoint "$CHECKPOINT" \
      --robocerebra_root "$BENCH_ROOT" \
      --init_files_root "$INIT_FILES_ROOT" \
      --task_types "[\"${task_name}\"]" \
      --num_trials_per_task "$NUM_TRIALS" \
      --local_log_dir "$EVAL_LOG_DIR" \
      --rollout_dir "$rollout_dir" \
      --run_id_note "$job_note"
    )
    if [[ -n "$TASK_SUITE_NAME" ]]; then
      eval_args+=(--task_suite_name "$TASK_SUITE_NAME")
    fi
    python eval_openvla.py "${eval_args[@]}" > "$log_path" 2>&1
  ) &
}

for raw_task in "${TASK_ARRAY[@]}"; do
  task="$(trim "$raw_task")"
  gpu="${GPU_ARRAY[$((gpu_cursor % ${#GPU_ARRAY[@]}))]}"
  launch_eval_job "$task" "$gpu"
  gpu_cursor=$((gpu_cursor + 1))
  active_jobs=$((active_jobs + 1))

  if (( active_jobs >= ${#GPU_ARRAY[@]} )); then
    if ! wait -n; then
      failed_jobs=1
    fi
    active_jobs=$((active_jobs - 1))
  fi
done

while (( active_jobs > 0 )); do
  if ! wait -n; then
    failed_jobs=1
  fi
  active_jobs=$((active_jobs - 1))
done

python "${EVAL_DIR}/summarize_eval_sweep.py" \
  --manifest "$MANIFEST" \
  --output-csv "$SUMMARY_CSV" \
  --output-md "$SUMMARY_MD" | tee -a "$MASTER_LOG"

echo "Manifest: ${MANIFEST}" | tee -a "$MASTER_LOG"
echo "Summary CSV: ${SUMMARY_CSV}" | tee -a "$MASTER_LOG"
echo "Summary MD: ${SUMMARY_MD}" | tee -a "$MASTER_LOG"
echo "Master log: ${MASTER_LOG}" | tee -a "$MASTER_LOG"

if (( failed_jobs != 0 )); then
  echo "OpenVLA reference evaluation completed with at least one failure." | tee -a "$MASTER_LOG"
  exit 1
fi

echo "OpenVLA reference evaluation completed successfully." | tee -a "$MASTER_LOG"
