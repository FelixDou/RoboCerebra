#!/usr/bin/env bash
set -u -o pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
EVAL_DIR="${REPO_ROOT}/evaluation"

usage() {
  cat <<'EOF'
Usage:
  bash evaluation/run_checkpoint_sweep.sh \
    --bench-root /path/to/RoboCerebraBench \
    --gpu-ids 0,1 \
    --checkpoint-group pi0libero /path/to/run_a 050000,100000,145000,150000 \
    --checkpoint-group pi0base /path/to/run_b 050000,100000,150000,180000

Required:
  --bench-root PATH
      RoboCerebra benchmark root.

  --checkpoint-group LABEL RUN_DIR STEPS
      Add a checkpoint group. STEPS is a comma-separated list of checkpoint
      directory names, for example 050000,100000,145000,150000.
      You can pass this flag multiple times.

Optional:
  --init-files-root PATH
      Defaults to <bench-root>/init_files

  --model-family NAME
      Defaults to pi0

  --task-types CSV
      Defaults to all six RoboCerebra task categories:
      Ideal,Random_Disturbance,Mix,Observation_Mismatching,Memory_Execution,Memory_Exploration

  --num-trials N
      Defaults to 5

  --gpu-ids CSV
      Comma-separated visible GPU ids to round-robin over. Defaults to 0.

  --eval-log-dir PATH
      Defaults to <repo>/experiments/logs

  --rollout-root PATH
      Defaults to sibling directory next to eval-log-dir named eval_rollouts

  --run-note-prefix TEXT
      Prefix used for generated job names and summary files. Defaults to sweep

Example:
  bash evaluation/run_checkpoint_sweep.sh \
    --bench-root /gs/bs/tga-shinoda/felid/robocerebra_data/RoboCerebraBench \
    --eval-log-dir /gs/bs/tga-shinoda/felid/eval_logs \
    --rollout-root /gs/bs/tga-shinoda/felid/eval_rollouts \
    --gpu-ids 0 \
    --checkpoint-group pi0libero /gs/bs/.../robocerebra_pi0_libero... 050000,100000,145000,150000 \
    --checkpoint-group pi0base /gs/bs/.../robocerebra_pi0_base... 050000,100000,150000,180000
EOF
}

MODEL_FAMILY="pi0"
NUM_TRIALS="5"
TASK_TYPES="Ideal,Random_Disturbance,Mix,Observation_Mismatching,Memory_Execution,Memory_Exploration"
GPU_IDS="0"
BENCH_ROOT=""
INIT_FILES_ROOT=""
EVAL_LOG_DIR="${REPO_ROOT}/experiments/logs"
ROLLOUT_ROOT=""
RUN_NOTE_PREFIX="sweep"

declare -a GROUP_LABELS=()
declare -a GROUP_RUN_DIRS=()
declare -a GROUP_STEPS=()

while (($#)); do
  case "$1" in
    --bench-root)
      BENCH_ROOT="$2"
      shift 2
      ;;
    --init-files-root)
      INIT_FILES_ROOT="$2"
      shift 2
      ;;
    --model-family)
      MODEL_FAMILY="$2"
      shift 2
      ;;
    --task-types)
      TASK_TYPES="$2"
      shift 2
      ;;
    --num-trials)
      NUM_TRIALS="$2"
      shift 2
      ;;
    --gpu-ids)
      GPU_IDS="$2"
      shift 2
      ;;
    --eval-log-dir)
      EVAL_LOG_DIR="$2"
      shift 2
      ;;
    --rollout-root)
      ROLLOUT_ROOT="$2"
      shift 2
      ;;
    --run-note-prefix)
      RUN_NOTE_PREFIX="$2"
      shift 2
      ;;
    --checkpoint-group)
      if (($# < 4)); then
        echo "error: --checkpoint-group expects LABEL RUN_DIR STEPS_CSV" >&2
        usage
        exit 1
      fi
      GROUP_LABELS+=("$2")
      GROUP_RUN_DIRS+=("$3")
      GROUP_STEPS+=("$4")
      shift 4
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      echo "error: unknown argument: $1" >&2
      usage
      exit 1
      ;;
  esac
done

if [[ -z "$BENCH_ROOT" ]]; then
  echo "error: --bench-root is required" >&2
  exit 1
fi

if ((${#GROUP_LABELS[@]} == 0)); then
  echo "error: at least one --checkpoint-group is required" >&2
  exit 1
fi

if [[ -z "$INIT_FILES_ROOT" ]]; then
  INIT_FILES_ROOT="${BENCH_ROOT}/init_files"
fi

if [[ -z "$ROLLOUT_ROOT" ]]; then
  ROLLOUT_ROOT="$(cd "${EVAL_LOG_DIR}/.." 2>/dev/null && pwd || true)"
  if [[ -z "$ROLLOUT_ROOT" ]]; then
    ROLLOUT_ROOT="${REPO_ROOT}/eval_rollouts"
  else
    ROLLOUT_ROOT="${ROLLOUT_ROOT}/eval_rollouts"
  fi
fi

mkdir -p "$EVAL_LOG_DIR" "$ROLLOUT_ROOT"

IFS=',' read -r -a TASK_ARRAY <<< "$TASK_TYPES"
IFS=',' read -r -a GPU_ARRAY <<< "$GPU_IDS"

if ((${#GPU_ARRAY[@]} == 0)); then
  echo "error: no GPU ids parsed from --gpu-ids" >&2
  exit 1
fi

ensure_robosuite_macros() {
  python - <<'PY'
import sysconfig
from pathlib import Path

macros_path = Path(sysconfig.get_paths()["purelib"]) / "robosuite" / "macros_private.py"
macros_path.parent.mkdir(parents=True, exist_ok=True)
content = (
    "# Auto-generated by evaluation/run_checkpoint_sweep.sh\n"
    "# Disable robosuite file logging on shared cluster nodes.\n"
    "FILE_LOGGING_LEVEL = None\n"
)
current = macros_path.read_text(errors="ignore") if macros_path.exists() else ""
if current != content:
    macros_path.write_text(content)
print(macros_path)
PY
}

trim() {
  local value="$1"
  value="${value#"${value%%[![:space:]]*}"}"
  value="${value%"${value##*[![:space:]]}"}"
  printf '%s' "$value"
}

task_slug() {
  local task_name="$1"
  task_name="${task_name// /_}"
  task_name="${task_name//\//_}"
  printf '%s' "$task_name"
}

DATE_TAG="$(date +%Y%m%d_%H%M%S)"
MASTER_LOG="${EVAL_LOG_DIR}/${RUN_NOTE_PREFIX}_${MODEL_FAMILY}_checkpoint_sweep_${DATE_TAG}.log"
MANIFEST="${EVAL_LOG_DIR}/${RUN_NOTE_PREFIX}_${MODEL_FAMILY}_checkpoint_sweep_${DATE_TAG}_manifest.tsv"
SUMMARY_CSV="${EVAL_LOG_DIR}/${RUN_NOTE_PREFIX}_${MODEL_FAMILY}_checkpoint_sweep_${DATE_TAG}_summary.csv"
SUMMARY_MD="${EVAL_LOG_DIR}/${RUN_NOTE_PREFIX}_${MODEL_FAMILY}_checkpoint_sweep_${DATE_TAG}_summary.md"

printf 'label\tstep\ttask\tgpu\tcheckpoint_path\tlog_path\trollout_dir\n' > "$MANIFEST"

echo "Starting checkpoint sweep at ${DATE_TAG}" | tee "$MASTER_LOG"
echo "Bench root: ${BENCH_ROOT}" | tee -a "$MASTER_LOG"
echo "Init files root: ${INIT_FILES_ROOT}" | tee -a "$MASTER_LOG"
echo "Model family: ${MODEL_FAMILY}" | tee -a "$MASTER_LOG"
echo "Task types: ${TASK_TYPES}" | tee -a "$MASTER_LOG"
echo "Num trials: ${NUM_TRIALS}" | tee -a "$MASTER_LOG"
echo "GPU ids: ${GPU_IDS}" | tee -a "$MASTER_LOG"
echo "Eval log dir: ${EVAL_LOG_DIR}" | tee -a "$MASTER_LOG"
echo "Rollout root: ${ROLLOUT_ROOT}" | tee -a "$MASTER_LOG"

ROBOSUITE_MACROS_PATH="$(ensure_robosuite_macros)"
echo "Robosuite macros path: ${ROBOSUITE_MACROS_PATH}" | tee -a "$MASTER_LOG"

active_jobs=0
gpu_cursor=0
failed_jobs=0

launch_eval_job() {
  local label="$1"
  local step="$2"
  local task="$3"
  local gpu="$4"
  local checkpoint_path="$5"

  local task_name
  task_name="$(trim "$task")"
  local task_name_slug
  task_name_slug="$(task_slug "$task_name")"
  local job_note="${RUN_NOTE_PREFIX}_${label}_${step}_${task_name_slug}_${DATE_TAG}"
  local log_path="${EVAL_LOG_DIR}/${job_note}.log"
  local rollout_dir="${ROLLOUT_ROOT}/${job_note}"

  mkdir -p "$rollout_dir"
  printf '%s\t%s\t%s\t%s\t%s\t%s\t%s\n' \
    "$label" "$step" "$task_name" "$gpu" "$checkpoint_path" "$log_path" "$rollout_dir" >> "$MANIFEST"

  echo "Launching label=${label} step=${step} task=${task_name} gpu=${gpu}" | tee -a "$MASTER_LOG"
  echo "  checkpoint=${checkpoint_path}" | tee -a "$MASTER_LOG"
  echo "  log=${log_path}" | tee -a "$MASTER_LOG"

  (
    cd "$EVAL_DIR"
    export CUDA_VISIBLE_DEVICES="$gpu"
    export MUJOCO_GL="${MUJOCO_GL:-egl}"
    python eval_openvla.py \
      --model_family "$MODEL_FAMILY" \
      --pretrained_checkpoint "$checkpoint_path" \
      --robocerebra_root "$BENCH_ROOT" \
      --init_files_root "$INIT_FILES_ROOT" \
      --task_types "[\"${task_name}\"]" \
      --num_trials_per_task "$NUM_TRIALS" \
      --local_log_dir "$EVAL_LOG_DIR" \
      --rollout_dir "$rollout_dir" \
      --run_id_note "$job_note" \
      > "$log_path" 2>&1
  ) &
}

for idx in "${!GROUP_LABELS[@]}"; do
  label="${GROUP_LABELS[$idx]}"
  run_dir="${GROUP_RUN_DIRS[$idx]}"
  step_csv="${GROUP_STEPS[$idx]}"

  IFS=',' read -r -a step_array <<< "$step_csv"
  for raw_step in "${step_array[@]}"; do
    step="$(trim "$raw_step")"
    checkpoint_path="${run_dir}/checkpoints/${step}/pretrained_model"

    if [[ ! -d "$checkpoint_path" ]]; then
      echo "Missing checkpoint, skipping: ${checkpoint_path}" | tee -a "$MASTER_LOG"
      failed_jobs=1
      continue
    fi

    for raw_task in "${TASK_ARRAY[@]}"; do
      task="$(trim "$raw_task")"
      gpu="${GPU_ARRAY[$((gpu_cursor % ${#GPU_ARRAY[@]}))]}"
      launch_eval_job "$label" "$step" "$task" "$gpu" "$checkpoint_path"
      gpu_cursor=$((gpu_cursor + 1))
      active_jobs=$((active_jobs + 1))

      if (( active_jobs >= ${#GPU_ARRAY[@]} )); then
        if ! wait -n; then
          failed_jobs=1
        fi
        active_jobs=$((active_jobs - 1))
      fi
    done
  done
done

while (( active_jobs > 0 )); do
  if ! wait -n; then
    failed_jobs=1
  fi
  active_jobs=$((active_jobs - 1))
done

python "${EVAL_DIR}/summarize_eval_sweep.py" \
  --manifest "$MANIFEST" \
  --output-csv "$SUMMARY_CSV" \
  --output-md "$SUMMARY_MD" | tee -a "$MASTER_LOG"

echo "Manifest: ${MANIFEST}" | tee -a "$MASTER_LOG"
echo "Summary CSV: ${SUMMARY_CSV}" | tee -a "$MASTER_LOG"
echo "Summary MD: ${SUMMARY_MD}" | tee -a "$MASTER_LOG"
echo "Master log: ${MASTER_LOG}" | tee -a "$MASTER_LOG"

if (( failed_jobs != 0 )); then
  echo "Checkpoint sweep completed with at least one failure." | tee -a "$MASTER_LOG"
  exit 1
fi

echo "Checkpoint sweep completed successfully." | tee -a "$MASTER_LOG"
