#!/usr/bin/env python3
"""
Summarize a checkpoint sweep launched by run_checkpoint_sweep.sh.
"""

from __future__ import annotations

import argparse
import csv
import json
import re
from pathlib import Path

RESULTS_RE = re.compile(r"Results saved to (.+_results\.json)")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Summarize RoboCerebra checkpoint sweep results.")
    parser.add_argument("--manifest", required=True, help="Manifest TSV generated by run_checkpoint_sweep.sh")
    parser.add_argument("--output-csv", required=True, help="Path to write the flat CSV summary")
    parser.add_argument("--output-md", required=True, help="Path to write the Markdown summary table")
    return parser.parse_args()


def load_manifest(path: Path) -> list[dict[str, str]]:
    with path.open("r", encoding="utf-8") as handle:
        return list(csv.DictReader(handle, delimiter="\t"))


def find_results_path(log_path: Path) -> Path | None:
    if not log_path.exists():
      return None

    results_path: Path | None = None
    with log_path.open("r", encoding="utf-8", errors="replace") as handle:
        for line in handle:
            match = RESULTS_RE.search(line)
            if match:
                results_path = Path(match.group(1).strip())
    return results_path


def extract_metrics(results_path: Path, task_name: str) -> dict[str, object]:
    with results_path.open("r", encoding="utf-8") as handle:
        payload = json.load(handle)

    task_results = payload.get("results_by_task_type", {}).get(task_name)
    if task_results is None:
        raise KeyError(f"Task `{task_name}` not found in {results_path}")

    overall = payload.get("overall_results", {})
    return {
        "episodes": task_results.get("episodes", overall.get("total_episodes")),
        "successes": task_results.get("successes", overall.get("total_successes")),
        "success_rate": task_results.get("success_rate", overall.get("overall_success_rate")),
        "agent_subtasks": task_results.get("agent_subtasks", overall.get("total_agent_subtasks")),
        "possible_subtasks": task_results.get("possible_subtasks", overall.get("total_possible_subtasks")),
        "subtask_rate": task_results.get("subtask_rate", overall.get("overall_subtask_rate")),
    }


def sort_key(row: dict[str, object]) -> tuple[str, int, str]:
    label = str(row["label"])
    step = str(row["step"])
    numeric_step = int(step) if step.isdigit() else -1
    task = str(row["task"])
    return (label, numeric_step, task)


def write_csv(rows: list[dict[str, object]], output_path: Path) -> None:
    fieldnames = [
        "label",
        "step",
        "task",
        "status",
        "episodes",
        "successes",
        "success_rate",
        "agent_subtasks",
        "possible_subtasks",
        "subtask_rate",
        "checkpoint_path",
        "results_json",
        "log_path",
    ]
    with output_path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow({key: row.get(key, "") for key in fieldnames})


def write_markdown(rows: list[dict[str, object]], output_path: Path) -> None:
    lines = [
        "| Label | Step | Task | Status | Episode % | Successes | Subtask % | Subtasks |",
        "|---|---:|---|---|---:|---:|---:|---:|",
    ]
    for row in rows:
        episode_pct = ""
        subtask_pct = ""
        successes = ""
        subtasks = ""
        if row["status"] == "ok":
            episode_pct = f"{100.0 * float(row['success_rate']):.2f}"
            subtask_pct = f"{100.0 * float(row['subtask_rate']):.2f}"
            successes = f"{row['successes']}/{row['episodes']}"
            subtasks = f"{row['agent_subtasks']}/{row['possible_subtasks']}"
        lines.append(
            f"| {row['label']} | {row['step']} | {row['task']} | {row['status']} | "
            f"{episode_pct} | {successes} | {subtask_pct} | {subtasks} |"
        )
    output_path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    args = parse_args()
    manifest_path = Path(args.manifest).expanduser().resolve()
    output_csv = Path(args.output_csv).expanduser().resolve()
    output_md = Path(args.output_md).expanduser().resolve()

    manifest_rows = load_manifest(manifest_path)
    summary_rows: list[dict[str, object]] = []

    for row in manifest_rows:
        log_path = Path(row["log_path"]).expanduser().resolve()
        checkpoint_path = Path(row["checkpoint_path"]).expanduser().resolve()
        result_row: dict[str, object] = {
            "label": row["label"],
            "step": row["step"],
            "task": row["task"],
            "status": "missing_log",
            "checkpoint_path": str(checkpoint_path),
            "results_json": "",
            "log_path": str(log_path),
        }

        if log_path.exists():
            result_row["status"] = "missing_results"
            results_path = find_results_path(log_path)
            if results_path is not None and results_path.exists():
                result_row["results_json"] = str(results_path.resolve())
                result_row["status"] = "ok"
                try:
                    result_row.update(extract_metrics(results_path, row["task"]))
                except Exception as exc:  # pragma: no cover - best effort summary
                    result_row["status"] = f"parse_error: {exc}"
        summary_rows.append(result_row)

    summary_rows.sort(key=sort_key)
    write_csv(summary_rows, output_csv)
    write_markdown(summary_rows, output_md)

    print(f"Wrote CSV summary to {output_csv}")
    print(f"Wrote Markdown summary to {output_md}")
    for row in summary_rows:
        if row["status"] == "ok":
            print(
                f"{row['label']} step {row['step']} task {row['task']}: "
                f"episode={100.0 * float(row['success_rate']):.2f}% "
                f"subtask={100.0 * float(row['subtask_rate']):.2f}%"
            )
        else:
            print(f"{row['label']} step {row['step']} task {row['task']}: {row['status']}")


if __name__ == "__main__":
    main()
